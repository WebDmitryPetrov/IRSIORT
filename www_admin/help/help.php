«Отсканировано», «Отметить как недействительный», «Отклонить выполнение и
перенести срок», «Обсуждение».<o:p></o:p></span></b></p>

<p class=MsoBodyText><b><span style='font-size:14.0pt;font-family:"Times New Roman";
mso-ansi-language:RU'>«Перенести срок выполнения»</span></b><span
style='font-size:14.0pt;font-family:"Times New Roman";mso-ansi-language:RU'> -
служит для отправки <u>ответственным сотрудником управления</u> мотивированного
запроса на перенос срока выполнения по документу;<o:p></o:p></span></p>

<p class=MsoBodyText><b><span style='font-size:14.0pt;font-family:"Times New Roman";
mso-ansi-language:RU'>«Отметить как выполненное»</span></b><span
style='font-size:14.0pt;font-family:"Times New Roman";mso-ansi-language:RU'> -
служит для отметки <u>ответственным сотрудником управления</u> факта выполнения
документа;<o:p></o:p></span></p>

<p class=MsoBodyText><b><span style='font-size:14.0pt;font-family:"Times New Roman";
mso-ansi-language:RU'>«Запрос на перенос срока выполнения» - </span></b><span
style='font-size:14.0pt;font-family:"Times New Roman";mso-ansi-language:RU'>служит
для переноса срока <u>ответственным сотрудником управления;<o:p></o:p></u></span></p>

<p class=MsoBodyText><b><span style='font-size:14.0pt;font-family:"Times New Roman";
mso-ansi-language:RU'>«Снять документ с контроля» - </span></b><span
