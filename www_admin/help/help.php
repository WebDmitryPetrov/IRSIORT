<p class=MsoBodyText><b><span style='font-size:14.0pt;font-family:"Times New Roman";
mso-ansi-language:RU'>«Отсканировано» </span></b><span style='font-size:14.0pt;
font-family:"Times New Roman";mso-ansi-language:RU'>- служит для отметки <u>ответственным
